// ─── HELPERS ──────────────────────────────────────────────────────────────────

export const getLocationsByName = (name: string): Location[] =>
  CAMPUS_LOCATIONS.filter((loc) =>
    loc.name.toLowerCase().includes(name.toLowerCase()),
  );

export const getLocationByAbbreviation = (
  abbreviation: string,
): Location | undefined =>
  CAMPUS_LOCATIONS.find(
    (loc) => loc.abbreviation.toLowerCase() === abbreviation.toLowerCase(),
  );

export const getMatchingPickupLocations = (query: string): Location[] => {
  const lowerQuery = query.toLowerCase();
  return CAMPUS_LOCATIONS.filter(
    (loc) =>
      loc.name.toLowerCase().includes(lowerQuery) ||
      loc.abbreviation.toLowerCase().includes(lowerQuery),
  );
};
